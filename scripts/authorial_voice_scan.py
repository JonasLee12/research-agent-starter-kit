#!/usr/bin/env python3
"""Scan academic prose for authorial-voice and generic AI-style risks.

This is a writing-quality helper, not an AI detector.
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from dataclasses import dataclass
from datetime import datetime, timezone
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
OUT_DIR = ROOT / "audit-reports" / "authorial-voice"
EVENT_LOG = ROOT / "research-wiki" / "SESSION_EVENT_LOG.jsonl"


@dataclass
class Finding:
    severity: str
    category: str
    line: int
    evidence: str
    action: str


CONNECTIVE_PATTERNS: list[tuple[str, str]] = [
    (r"\bthis\s+(?:distinction\s+)?matters\s+because\b", "repeated connective template"),
    (r"\btaken\s+together\b", "repeated connective template"),
    (r"\btogether,\s+these\b", "repeated connective template"),
    (r"\bthese\s+(?:sources|studies)\s+(?:show|suggest|support|provide|indicate)\b", "source-aggregation template"),
    (r"\b(?:this|these)\s+\w+\s+(?:helps?|support|supports|frames|specif(?:y|ies)|structure|structures)\b", "formulaic warrant connector"),
    (r"\bis\s+useful\s+(?:for|because)\b", "formulaic usefulness warrant"),
]

LEXEME_CAPS: list[tuple[str, str, int]] = [
    (r"\bconditions?\b", "overused framing lexeme", 8),
    (r"\bsensiti[sz](?:e|es|ed|ing)\b", "overused framing lexeme", 5),
    (r"\bbounded\b", "overused framing lexeme", 5),
    (r"\bcautious(?:ly)?\b", "overused framing lexeme", 5),
    (r"\bdeliberate(?:ly)?\b", "overused framing lexeme", 5),
]

CODA_PATTERNS: list[tuple[str, str]] = [
    (r"\bteacher\s+interviews?\b", "repeated methodology coda"),
    (r"\binterview\s+evidence\b", "repeated methodology coda"),
    (r"\bteachers'? own reasoning should still determine\b", "repeated methodology coda"),
    (r"\bconcept\s+card\b", "repeated methodology coda"),
    (r"\bthis\s+(?:project|study|dissertation|thesis|article|report)\b", "repeated project self-reference coda"),
]



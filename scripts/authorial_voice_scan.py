def resolve(path_text: str) -> Path:
    path = Path(path_text).expanduser()
    return path if path.is_absolute() else ROOT / path


def rel(path: Path) -> str:
    try:
        return str(path.resolve().relative_to(ROOT))
    except ValueError:
        return str(path.resolve())


def scan(text: str) -> list[Finding]:
    findings: list[Finding] = []
    in_fence = False
    for index, line in enumerate(text.splitlines(), 1):
        stripped = line.strip()
        if stripped.startswith("```"):
            in_fence = not in_fence
            continue
        quoted_or_fenced = in_fence or stripped.startswith(">")
        for pattern, severity, category, action in PATTERNS:
            if quoted_or_fenced and severity == "WARN":
                continue
            if re.search(pattern, line, flags=re.I):
                findings.append(Finding(severity, category, index, line.strip()[:220], action))
    return findings


def status(findings: list[Finding]) -> str:
    if any(item.severity == "HOLD" for item in findings):
        return "HOLD"
    if findings:
        return "WARN"
    return "PASS"


def render(target: Path, findings: list[Finding]) -> str:
    result = status(findings)
    lines = [
        "# Authorial Voice Scan",
        "",
        f"Generated: {datetime.now(timezone.utc).isoformat(timespec='seconds')}",
        f"Target: `{rel(target)}`",
        f"Status: `{result}`",
        "",
        "## Findings",
        "",
        "| Severity | Category | Line | Evidence | Action |",
        "|---|---|---:|---|---|",
    ]
    if findings:
        for item in findings:
            evidence = item.evidence.replace("|", "\\|")
            action = item.action.replace("|", "\\|")
            lines.append(f"| {item.severity} | {item.category} | {item.line} | {evidence} | {action} |")
    else:
        lines.append("| PASS | no finding | - | - | - |")
    lines.extend(
        [
            "",
            "## Boundary",
            "",
            "This scan flags authorial-voice and generic AI-style risks. A `HOLD` result is a workflow warning, not a judgement that text is AI-generated. This scan is not an AI detector, plagiarism detector, source-support check, compliance approval, or official institutional policy.",
            "",
        ]
    )
    return "\n".join(lines)


def append_event(target: Path, report: Path, result: str) -> None:
    EVENT_LOG.parent.mkdir(parents=True, exist_ok=True)
    event = {
        "timestamp": datetime.now(timezone.utc).isoformat(timespec="seconds"),
        "run_id": f"authorial-voice-scan-{datetime.now(timezone.utc).strftime('%Y-%m-%d')}",
        "window": "Production",
        "event_type": "quality_gate",
        "status": "completed",
        "result": result,
        "skill": "authorial-voice-integrity",
        "file": rel(report),
        "evidence": f"Authorial voice scan completed with result {result} for {rel(target)}.",
        "risk": "low" if result == "PASS" else "medium",
    }
    with EVENT_LOG.open("a", encoding="utf-8") as handle:
        handle.write(json.dumps(event, ensure_ascii=False) + "\n")


def main() -> int:
    parser = argparse.ArgumentParser(description="Run an authorial voice and generic AI-style scan.")
    parser.add_argument("--target", required=True)
    parser.add_argument("--output-dir", default=str(OUT_DIR))
    parser.add_argument("--strict", action="store_true", help="Return non-zero on HOLD.")
    parser.add_argument("--write-event", action="store_true", help="Append a local session-event log entry.")
    args = parser.parse_args()

    target = resolve(args.target)
    if not target.exists():
        print(f"Missing target: {target}", file=sys.stderr)
        return 2
    if not target.is_file():
        print(f"Target is not a file: {target}", file=sys.stderr)
        return 2
    text = target.read_text(encoding="utf-8", errors="replace")
    findings = scan(text)
    result = status(findings)

    out_dir = resolve(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    report = out_dir / f"Authorial_Voice_Scan_{target.stem}_{datetime.now().strftime('%Y-%m-%d_%H%M%S')}.md"
    report.write_text(render(target, findings), encoding="utf-8")
    if args.write_event:
        append_event(target, report, result)
    print(f"Report: {report}")
    print(f"Status: {result}")
    return 1 if args.strict and result == "HOLD" else 0


if __name__ == "__main__":
    raise SystemExit(main())

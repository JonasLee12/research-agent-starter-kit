#!/usr/bin/env python3
"""Scan research prose for authorial-voice and generic AI-style risks.

This helper is not an AI detector. It flags workflow risks such as
detector-evasion framing, disclosure hiding, chatbot residue, generic phrasing,
inflated vocabulary, and possible overclaiming.
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from dataclasses import dataclass
from datetime import datetime, timezone
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
OUT_DIR = ROOT / "audit-reports" / "authorial-voice"
EVENT_LOG = ROOT / "research-wiki" / "SESSION_EVENT_LOG.jsonl"


@dataclass
class Finding:
    severity: str
    category: str
    line: int
    evidence: str
    action: str


